# БЛОК ИМПОРТА
# Импорт необходимых модулей из библиотеки Flask
from flask import Flask, render_template
# Flask - класс для создания экземпляра веб-приложения
# render_template - функция для рендеринга HTML-шаблонов

# БЛОК ИНИЦИАЛИЗАЦИИ ПРИЛОЖЕНИЯ
# Создание экземпляра веб-приложения Flask
app = Flask(__name__)
# __name__ - имя текущего модуля, используется Flask для определения путей к ресурсам

# БЛОК МАРШРУТИЗАЦИИ
# Определение маршрута для корневого URL
@app.route('/')
# Декоратор связывает функцию с корневым путём веб-сервера
def hello_world():
    # Функция-обработчик корневого маршрута
    return render_template("index.html")
# Функция render_template ищет файл index.html в папке templates
# и возвращает сгенерированную HTML-страницу клиенту

# БЛОК ЗАПУСКА СЕРВЕРА
# Запуск встроенного сервера Flask
app.run("0.0.0.0")
# Параметр "0.0.0.0" указывает серверу принимать подключения на всех сетевых интерфейсах
# Это необходимо для доступа к приложению извне в среде Replit